import api.EdgeData;

public class Edge implements EdgeData {

    private int src;
    private int dst;
    private double weight;
    private int tag =0;

    public Edge(int src , int dst , double weight){
        this.src = src;
        this.dst = dst;
        this.weight = weight;
    }

    @Override
    public int getSrc() {
        return this.src;
    }

    @Override
    public int getDest() {
        return this.dst;
    }

    @Override
    public double getWeight() {
        return this.weight;
    }

    @Override
    public String getInfo() {
        return null;
    }

    @Override
    public void setInfo(String s) {

    }

    @Override
    public int getTag() {
        return this.tag;
    }

    @Override
    public void setTag(int t) {
        this.tag = t;
    }
}
